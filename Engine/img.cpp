void img::HP_Bar(const Vec2& topleft, float maxWidth, float height, float maxHealth, float currentHealth, Graphics& gfx)
{
	//Colors (goes slowly from RED at very low HP to YELLOW at around half HP to GREEN at full HP)
//Green ( equal to 255 if HP is 50% or over, then starts decreasing as HP decreases)
	int G = (int)((currentHealth / (maxHealth / 2.0f)) * 255.0f);
	if (G > 255) G = 255;
	else if (G < 0) G = 0;
	//Red ( equal to 0 if HP is full then starts increasing as HP decreases, max value is 255 at 50% HP or lower)
	int R = (int)((maxHealth - currentHealth) / (maxHealth / 2.0f) * 255.0f);
	if (R > 255) R = 255;
	else if (R < 0) R = 0;
	//Position
	const int x = (int)topleft.x;
	const int y = (int)topleft.y;
	//Width
	const float fWidth = maxWidth * (currentHealth / maxHealth);
	int nWidth = (int)fWidth;
	const int nHeight = (int)height;
	if (fWidth < 1.0f && fWidth > 0.0f) nWidth = 1;
	gfx.DrawRectDim(x, y, nWidth, nHeight, Colors::MakeRGB(R, G, 0));
}

void img::Gif6(int&slider, Graphics& gfx,  Vec2& centar, const std::string& vol1,const std::string& vol2, const std::string& vol3,const std::string& vol4, const std::string& vol5, const std::string& vol6)
{
	Surface s1 = Surface(vol1);
	Surface s2 = Surface(vol2); 
	Surface s3 = Surface(vol3);
	Surface s4 = Surface(vol4);
	Surface s5 = Surface(vol5);
	Surface s6 = Surface(vol6);
	
	
	slider++;

	if (slider > 0 && slider< 20)
	{
		gfx.DrawSprite(centar.x,centar.y, s1);
	}
	
	if (slider > 20 && slider < 30)

	{
		gfx.DrawSprite(centar.x, centar.y, s2);
	}


	if (slider > 30 && slider < 40)

	{
		gfx.DrawSprite(centar.x, centar.y, s3);
	}

	if (slider > 40 && slider < 50)

	{
		gfx.DrawSprite(centar.x, centar.y, s4);;
	}
	if (slider > 40 && slider < 50)

	{
		gfx.DrawSprite(centar.x, centar.y, s5);
	}
	if (slider > 50 && slider < 60)

	{
		gfx.DrawSprite(centar.x, centar.y, s6);;
	}
	
	slider = 0;
}
